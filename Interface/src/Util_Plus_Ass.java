public class Util_Plus_Ass {
    private String nom;
    private String prenom;
    private int nbe;

    public Util_Plus_Ass(String nom, String prenom, int nbe) {
        this.nom = nom;
        this.prenom = prenom;
        this.nbe = nbe;
    }

  

    public String getNom() {
        return nom;
    }

    public void setNom(String nom) {
        this.nom = nom;
    }

    public String getPrenom() {
        return prenom;
    }

    public void setPrenom(String prenom) {
        this.prenom = prenom;
    }

    public int getNbe() {
        return nbe;
    }

    public void setNbe(int nbe) {
        this.nbe = nbe;
    }
}
